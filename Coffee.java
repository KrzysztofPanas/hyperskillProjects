package machine.coffeemachine;

public class Coffee {

    int waterAmount;
    int milkAmount;
    int beansAmount;
    int cupsAmount;
    int moneyAmount;

    public Coffee(int waterAmount, int milkAmount, int beansAmount, int cupsAmount, int moneyAmount) {
        this.waterAmount = waterAmount;
        this.milkAmount = milkAmount;
        this.beansAmount = beansAmount;
        this.cupsAmount = cupsAmount;
        this.moneyAmount = moneyAmount;
    }
}
